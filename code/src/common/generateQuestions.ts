/**
 * 随机拼凑十万个为什么问题。
 * @param count 
 * @returns 
 */
 export function generateQuestions(count: number): string[] {
 
  // 获取所有主题和领域的映射
  const allTopics: { topic: string; domain: string }[] = [];
  for (const domain in topics) {
    topics[domain].forEach((topic) => allTopics.push({ topic, domain }));
  }

  const questions: Set<string> = new Set();
  const maxCombinations = prefixes.length * allTopics.length * Object.values(actions).flat().length;
  console.log(`最大可能组合数: ${maxCombinations}`);

  if (count > maxCombinations) {
    console.warn(`请求的问题数 (${count}) 超过最大组合数 (${maxCombinations})，可能会有重复`);
  }

  while (questions.size < count) {
    const prefix = prefixes[Math.floor(Math.random() * prefixes.length)];
    const { topic, domain } = allTopics[Math.floor(Math.random() * allTopics.length)];
    const domainActions = actions[domain];
    const action = domainActions[Math.floor(Math.random() * domainActions.length)];

    const question = `${prefix}${topic}${action}？`;
    questions.add(question);
  }

  return Array.from(questions);
}
